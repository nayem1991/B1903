public class AssignmentOperator {

    public static void main(String[] args) {
        int x =4;
        int y =7;

        x+=y; // x = x+y=11
        System.out.println ("x = "+x);

        x-=y;
        System.out.println ("x = "+x);
        x*=y;
        System.out.println ("x = "+x);
        x/=y;
        System.out.println ("x = "+x);
        x%=y;
        System.out.println ("x = "+x);

    }
}
